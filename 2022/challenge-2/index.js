function solvePartOne(input) {
    const rock = 1
    const paper = 2
    const scissors = 3
    const A = rock
    const B = paper
    const C = scissors
    const X = rock
    const Y = paper
    const Z = scissors

    function isWin(opponent, player) {
        if (opponent === A && player === Z) return 0
        if (opponent === C && player === Y) return 0
        if (opponent === B && player === X) return 0
        if (player === X && opponent === C) return 6
        if (player === Z && opponent === B) return 6
        if (player === Y && opponent === A) return 6
        if (player === opponent) return 3
    }

    let total = 0

    for (const item of input) {
        let opponent
        let player

        if (item[0] === 'A') opponent = A
        if (item[0] === 'B') opponent = B
        if (item[0] === 'C') opponent = C
        if (item[2] === 'X') player = X
        if (item[2] === 'Y') player = Y
        if (item[2] === 'Z') player = Z

        const matchResult = isWin(opponent, player)
        total += matchResult + player
    }
    return total
}

function solvePartTwo(input) {
    const rock = 1
    const paper = 2
    const scissors = 3
    const lost = 0
    const tie = 3
    const won = 6
    const A = rock
    const B = paper
    const C = scissors
    const X = lost
    const Y = tie
    const Z = won

    function solvePlayerChoice(opponent, result) {
        if (result === tie) {
            return opponent
        }
        if (result === lost) {
            if (opponent === A) return C
            if (opponent === B) return A
            if (opponent === C) return B
        }
        if (result === won) {
            if (opponent === A) return B
            if (opponent === B) return C
            if (opponent === C) return A
        }
    }

    let total = 0

    for (const item of input) {
        let opponent
        let result

        if (item[0] === 'A') opponent = A
        if (item[0] === 'B') opponent = B
        if (item[0] === 'C') opponent = C
        if (item[2] === 'X') result = X
        if (item[2] === 'Y') result = Y
        if (item[2] === 'Z') result = Z

        const player = solvePlayerChoice(opponent, result)
        total += player + result
    }
    return total
}

